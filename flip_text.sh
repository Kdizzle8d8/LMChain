#!/bin/bash
flip_text() {
    local input="$1"
    local flipped=""
    local length=${#input}

    declare -A flip_map=(
        ["a"]="…ê" ["b"]="q" ["c"]="…î" ["d"]="p" ["e"]="«ù" ["f"]="…ü" ["g"]="∆É" ["h"]="…•"
        ["i"]="·¥â" ["j"]="…æ" ["k"]=" û" ["l"]="l" ["m"]="…Ø" ["n"]="u" ["o"]="o" ["p"]="d"
        ["q"]="b" ["r"]="…π" ["s"]="s" ["t"]=" á" ["u"]="n" ["v"]=" å" ["w"]=" ç" ["x"]="x"
        ["y"]=" é" ["z"]="z" ["A"]="‚àÄ" ["B"]="êêí" ["C"]="∆Ü" ["D"]="·ó°" ["E"]="∆é" ["F"]="‚Ñ≤"
        ["G"]="‚ÖÅ" ["H"]="H" ["I"]="I" ["J"]="≈ø" ["K"]="‚ãä" ["L"]="À•" ["M"]="W" ["N"]="N"
        ["O"]="O" ["P"]="‘Ä" ["Q"]="Q" ["R"]="·¥ö" ["S"]="S" ["T"]="‚ä•" ["U"]="‚à©" ["V"]="Œõ"
        ["W"]="M" ["X"]="X" ["Y"]="‚ÖÑ" ["Z"]="Z" ["0"]="0" ["1"]="∆ñ" ["2"]="·ÑÖ" ["3"]="∆ê"
        ["4"]="„Ñ£" ["5"]="œõ" ["6"]="9" ["7"]="„Ñ•" ["8"]="8" ["9"]="6" ["!"]="¬°" ["?"]="¬ø"
        ["."]="Àô" [","]="'" ["'"]=","
    )

    for (( i=0; i<$length; i++ )); do
        char="${input:$i:1}"
        flipped="${flip_map[$char]:-$char}$flipped"
    done

    echo "$flipped"
}

if [ $# -eq 0 ]; then
    echo "Usage: $0 <text>"
    exit 1
fi

flip_text "$1"
